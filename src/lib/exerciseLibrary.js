// FUZZY MATCHING HELPER
// ═══════════════════════════════════════════════════════════════
// Strips weight amounts and parentheticals, then finds closest match.
// Useful for handling entries like "Goblet Squat (20 lb)" when the
// library has "Goblet Squat".

/**
 * Get exercise info with fuzzy matching.
 * Strips weight amounts and parenthetical details to find matches.
 *
 * @param {string} name - The exercise name (may include weight, reps, etc.)
 * @returns {object} - { description, video } or fallback if not found
 *
 * @example
 * getExerciseInfo("Goblet Squat (20 lb)") // matches "Goblet Squat"
 * getExerciseInfo("DB Row (20 lb)") // matches "DB Row"
 */
export function getExerciseInfo(name) {
  // First try exact match
  if (EXERCISE_LIBRARY[name]) {
    return EXERCISE_LIBRARY[name];
  }

  // Strip parentheticals and weight amounts
  const cleaned = name
    .replace(/\s*\([^)]*\)/g, "") // Remove anything in parentheses
    .replace(/\s*\d+\s*lb$/i, "") // Remove trailing weight like "20 lb"
    .trim();

  if (cleaned !== name && EXERCISE_LIBRARY[cleaned]) {
    return EXERCISE_LIBRARY[cleaned];
  }

  // Try a substring/prefix match (case-insensitive)
  const lowerName = name.toLowerCase();
  const cleanedLower = cleaned.toLowerCase();

  for (const [key, value] of Object.entries(EXERCISE_LIBRARY)) {
    const keyLower = key.toLowerCase();
    // Fuzzy: if cleaned name starts with or is contained in library key
    if (
      keyLower.includes(cleanedLower) ||
      cleanedLower.includes(keyLower)
    ) {
      return value;
    }
  }

  // Fallback: return a generic exercise placeholder
  return {
    description:
      "Exercise details not found in the library. Refer to your workout plan for specific instructions.",
    gif: null
  };
}

